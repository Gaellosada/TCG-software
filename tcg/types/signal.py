@dataclass(frozen=True)
class CompareCondition:
    """Binary comparison: ``lhs <op> rhs`` for ``gt|lt|ge|le|eq``."""

    op: Literal["gt", "lt", "ge", "le", "eq"]
    lhs: Operand
    rhs: Operand


@dataclass(frozen=True)
class CrossCondition:
    """Directional crossover between two series.

    ``count``/``window`` extend the primitive to "crossed at least ``count``
    times within the trailing ``window`` bars". A *crossing* is a single
    directional crossover (sign change of ``lhs - rhs`` in the condition's
    direction), counted **same-direction** only. The defaults ``count=1``,
    ``window=1`` reproduce today's single-bar crossover BYTE-IDENTICALLY: a
    trailing window of one bar holds only the current bar's crossing, so
    ``count>=1`` is exactly the current ``fired[t]`` pulse. The engine routes
    the default case to the unchanged crossover computation and only runs the
    trailing-cumsum path when ``count>1 or window>1``.
    """

    op: Literal["cross_above", "cross_below"]
    lhs: Operand
    rhs: Operand
    count: int = 1
    window: int = 1
    # Count MODE for ``count``:
    #   * ``"rolling"`` (default) — the trailing-``window`` count documented
    #     above (byte-identical to the historical behaviour: with
    #     ``count=1, window=1`` this is the single-bar crossover pulse).
    #   * ``"since_reset"`` — CUMULATIVE count of same-direction crossings SINCE
    #     the last reset, firing as an IMPULSE on exactly the ``count``-th
    #     crossing bar, then re-arming. The counter resets to 0 whenever the
    #     OWNING block's bound reset block (``Block.requires_reset_block_id``)
    #     FIRES (a reset-block firing bar, i.e. ``reset_truth & ~reset_nan``).
    #     ``window`` is IGNORED in this mode (the count is cumulative-since-reset,
    #     not windowed). Used to express an absolute reset-on-exit LADDER (fire on
    #     the Nth crossing since the ladder was last reset). When the block carries
    #     NO ``requires_reset_block_id`` the counter simply never resets (cumulative
    #     from bar 0). Default ``"rolling"`` reproduces today's behaviour exactly.
    count_mode: Literal["rolling", "since_reset"] = "rolling"


@dataclass(frozen=True)
class InRangeCondition:
    """``min <= operand <= max`` (all three resolved as series)."""

    op: Literal["in_range"]
    operand: Operand
    min: Operand
    max: Operand


@dataclass(frozen=True)
class RollingCondition:
    """``operand[t] <op> operand[t - lookback]`` for ``rolling_gt|rolling_lt``."""

    op: Literal["rolling_gt", "rolling_lt"]
    operand: Operand
    lookback: int


Condition = CompareCondition | CrossCondition | InRangeCondition | RollingCondition


